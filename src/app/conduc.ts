export class Conduc {
    id: number;
    ConducName: string;
    age: number;
    année_service: number;

    constructor(id: number, ConducName: string, age: number, année_service: number) {
        this.id = id;
        this.ConducName = ConducName;
        this.age = age;
        this.année_service = année_service;
    }
}

