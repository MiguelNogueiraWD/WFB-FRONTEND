export class Conduc {
    id: number;
    ConducName: string;
    age: number;
    annee_service: number;

    constructor(id: number, ConducName: string, age: number, annee_service: number) {
        this.id = id;
        this.ConducName = ConducName;
        this.age = age;
        this.annee_service = annee_service;
    }
}

